    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        String strNome = txtNome.getText();
        String strLogin = txtLogin.getText();
        String strSenha = txtSenha.getText();

        if (strNome.equals("") || 
                strLogin.equals("") || 
                strSenha.equals("")) {
            JOptionPane.showMessageDialog(
           null, "Preencha todos os campos");
        } else {
            String sql = "insert into usuarios "
                    + "(nome, login, senha) "
                    + "values (?, ?, ?)";
            Conector conector = new Conector();
            Connection con = conector.getConnection();
            try {
                PreparedStatement stm
                        = con.prepareStatement(sql);
                //Troca os "?" do sql para strNome
                stm.setString(1, strNome);
                stm.setString(2, strLogin);
                stm.setString(3, strSenha);
                stm.execute();
                stm.close();
                con.close();
                JOptionPane.showMessageDialog(
                        null, "Usu√°rio inserido!");
                txtNome.setText("");
                txtLogin.setText("");
                txtSenha.setText("");
                populaJTable();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_btnInserirActionPerformed

